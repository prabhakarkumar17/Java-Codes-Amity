class EvenOddTimes{
    public static void main(String[]args){
        int n=5;
        if(n%2==0){
            n=(n*3);
            System.out.println("The number is even & multiply by 3 = " +n);
        }
        else{
            n=(n*2);
             System.out.println("The number is Odd & multiply by 2 = "+n);
        }
    }
}
