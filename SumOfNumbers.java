import java.util.Scanner;

public class SumOfNumbers {
    public static void main(String[] args){
System.out.print("Initialize the number : ");
Scanner sc=new Scanner(System.in);
int a=sc.nextInt();
int sum=0; 
for(int i=1; i<=a;i++){
    sum=1+a;}
    System.out.print("Sum of number is :" + sum);
 }
    
}
